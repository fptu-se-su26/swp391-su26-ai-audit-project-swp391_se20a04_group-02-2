function removeDiacritics(str) {
  return str
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/đ/g, "d")
    .replace(/Đ/g, "D")
    .toLowerCase()
    .trim();
}

/**
 * Given a province text input, return the normalized key (e.g. "Ha Noi")
 * that maps to VN_DISTRICTS and VIETNAM_PROVINCES values.
 * Returns null if no match.
 */
export function matchProvince(text) {
  if (!text) return null;
  const normalized = removeDiacritics(text);
  if (PROVINCE_ALIASES[normalized]) return PROVINCE_ALIASES[normalized];
  // Also try the text as-is (if user typed the canonical English key)
  const directMatch = Object.keys(VN_DISTRICTS).find(
    k => removeDiacritics(k) === normalized
  );
  return directMatch || null;
}

/**
 * Get districts array for a given province text.
 * Returns [] if not found.
 */
export function getDistricts(provinceText) {
  const key = matchProvince(provinceText);
  return key ? VN_DISTRICTS[key] : [];
}

/**
 * Get wards array for a given district label.
 * Returns [] if not found.
 */
export function getWards(districtLabel) {
  return VN_WARDS[districtLabel] || [];
}
